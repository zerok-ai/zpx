#!/bin/bash
THIS_DIR=$( cd -- "$( dirname -- "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )
source $THIS_DIR/variables.sh

helpFunction()
{
   echo ""
   echo 'Usage: $0'

   exit 1 # Exit script after printing help
}

while [ $# -ne 0 ]; do
    case "$1" in
        -h)
             helpFunction
             exit 2
             ;;
        -*)
             echo "Unknown option: $1" >&2
             helpFunction
             exit 2
             ;;
        *)
             break
             ;;
    esac
done

##Delete px from current context
px delete

##Setup Operator
$SCRIPTS_DIR/setup-ingress.sh predeploy

##Setup Operator
$SCRIPTS_DIR/setup-operator.sh

##Postsetup Backend
$SCRIPTS_DIR/postsetup-backend.sh